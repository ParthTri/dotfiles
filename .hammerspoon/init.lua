-- Set app to full screen with padding
hs.hotkey.bind(hyper, "Space", function()
  local win = hs.window.focusedWindow()
  local f = win:frame()
  local screen = win:screen()
  local max = screen:frame()

  f.x = max.x + 10 
  f.y = max.y + 10
  f.w = max.w - 20
  f.h = max.h - 20
  win:setFrame(f)
end)
